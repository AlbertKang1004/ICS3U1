package ca.elearningontario.tvdsb.day01;

// Create a program that uses each of the control codes on the lesson tab

public class Practice1_1 {

	public static void main(String[] args) {
	
		System.out.print("New\nline\n"); // \n --> New line
		System.out.print("Return\rcharacter\r"); // \r --> Return character
		System.out.println("\tTab"); // \t --> Tab
		System.out.println("Backspace\b"); // \b --> Backspace
		System.out.println("Quotation \"marks\""); // \" or \' --> Quotation marks
		System.out.println("Backslash \\"); // \\ --> Backslash

	}

}
